#include "3-calc.h"

/**
 * op_add - returns the sum of two integers
 *
 * @a: the first integer to sum
 * @b: the second integer to sum
 *
 * Return: the sum of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the substraction of two integers
 *
 * @a: the first integer to operate
 * @b: the second integer to operate
 *
 * Return: the result of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the multiplication of two integers
 *
 * @a: the first integer to operate
 * @b: the second integer to operate
 *
 * Return: the result of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the int division of two integers
 *
 * @a: the first integer to operate
 * @b: the second integer to operate
 *
 * Return: the result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the int division of two integers
 *
 * @a: the first integer to operate
 * @b: the second integer to operate
 *
 * Return: the result of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
